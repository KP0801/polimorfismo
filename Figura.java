public class Figura {
      private int ancho;
      private int   largo;
    
      
public Figura(int ancho,int largo){
    this.ancho=ancho;
    this.largo=largo;
}

public int getAncho(){
    return ancho;
}

public void setAncho(int ancho){
    this.ancho=ancho;
}

public int getlargo(){
    return largo;
}
public void setLargo(int largo){
    this.largo=largo;
}


}
